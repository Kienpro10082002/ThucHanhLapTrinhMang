    private void btnChuyenDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenDoiActionPerformed
        String chuoi = txtChuoi.getText(); //Lấy số chuỗi nhập vào
        String ketQua = "";
        try {
            //SOCKET nhận tham số địa chỉ của HOST và PORT
            socket = new Socket("127.0.0.1", 1234);
            out = new PrintWriter(socket.getOutputStream(), true);
            in = new Scanner(socket.getInputStream());
            //Truyền chuỗi lên SERVER để xử lý
            out.println(chuoi);
            //Nhận chuỗi kết quả từ SERVER
            ketQua = in.nextLine().trim();
            txtKetQua.setText(ketQua);
            socket.close();
        } catch(Exception ex) {
            try {
                if(socket != null)
                socket.close();
            } catch(Exception e) {
                e.printStackTrace();
            }
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnChuyenDoiActionPerformed

